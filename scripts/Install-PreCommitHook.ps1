# VersionTag: 2605.B5.V46.0
<#
.SYNOPSIS
    Install a git pre-commit hook that runs the SIN scanner against staged PS files.
.DESCRIPTION
    Writes .git/hooks/pre-commit (no extension) which shells out to pwsh and
    runs Invoke-SINPatternScanner.ps1 against every staged .ps1/.psm1/.psd1 file.
    Aborts the commit with non-zero exit if any blocking SIN regression is found.
    Operator can bypass with `git commit --no-verify` (discouraged).
#>
[CmdletBinding()]
param(
    [string]$RepoRoot = (Resolve-Path -LiteralPath (Join-Path $PSScriptRoot '..')).Path,
    [switch]$Force
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

$gitDir = Join-Path $RepoRoot '.git'
if (-not (Test-Path -LiteralPath $gitDir)) {
    throw "Not a git repository: $RepoRoot (no .git folder)"
}
$hooksDir = Join-Path $gitDir 'hooks'
if (-not (Test-Path -LiteralPath $hooksDir)) {
    New-Item -ItemType Directory -Path $hooksDir | Out-Null
}
$hookPath = Join-Path $hooksDir 'pre-commit'
if ((Test-Path -LiteralPath $hookPath) -and -not $Force) {
    Write-Warning "Pre-commit hook already exists: $hookPath. Use -Force to overwrite."
    return
}

$hookBody = @'
#!/usr/bin/env sh
# VersionTag: 2605.B5.V46.0
# Auto-generated by scripts/Install-PreCommitHook.ps1 -- runs secret scanner, UI event safety scanner, and SIN scanner.
exec pwsh -NoProfile -ExecutionPolicy Bypass -Command "
  Set-StrictMode -Version Latest;
    \$stagedAll = @(git diff --cached --name-only --diff-filter=ACMR);
    if (@(\$stagedAll).Count -eq 0) { exit 0 }
    Write-Host '[pre-commit] Running secret leak scanner on staged files...' -ForegroundColor Cyan;
    & .\scripts\Invoke-SecretLeakScanner.ps1 -IncludeFiles \$stagedAll -FailOnFindings;
    if (\$LASTEXITCODE -ne 0) { exit \$LASTEXITCODE }
    Write-Host '[pre-commit] Running UI event safety scan...' -ForegroundColor Cyan;
    & .\tests\Invoke-UIEventSafetyScan.ps1 -WorkspacePath .;
    if (\$LASTEXITCODE -ne 0) { exit \$LASTEXITCODE }
    \$staged = @(\$stagedAll | Where-Object { \$_ -match '\.(ps1|psm1|psd1)\$' });
    if (@(\$staged).Count -eq 0) { exit 0 }
  Write-Host '[pre-commit] Running SIN scanner on staged PS files...' -ForegroundColor Cyan;
    & .\tests\Invoke-SINPatternScanner.ps1 -IncludeFiles \$staged -RatchetMode Permissive -BaselineJson config\sin-baseline.json -OutputJson reports\sin-scan-precommit.json -FailOnCritical;
  exit \$LASTEXITCODE
"
'@

# Use UTF8 *without* BOM for shell scripts (BOM breaks /usr/bin/env on git-bash)
[IO.File]::WriteAllText($hookPath, $hookBody, (New-Object System.Text.UTF8Encoding $false))

# Best-effort: mark executable on systems that support it (no-op on NTFS)
try { & git update-index --chmod=+x .git/hooks/pre-commit 2>$null } catch { <# ignore on Windows #> }

Write-Host "Installed pre-commit hook: $hookPath" -ForegroundColor Green
Write-Host "Bypass with: git commit --no-verify  (discouraged)" -ForegroundColor Yellow

