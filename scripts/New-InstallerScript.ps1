    Generates a self-contained PowerShell installer script that helps users
    install missing modules, the latest PowerShell version and the latest
    .NET SDK for their operating system.

.DESCRIPTION
    Reads the most recent module-dependency-matrix JSON from ~REPORTS to
    discover missing or version-mismatched modules. Produces a single .ps1
    file in ~DOWNLOADS\INSTALLER-Scripts that the user can run on any
    target machine to remediate gaps.

    The generated script:
      - Installs / updates missing PowerShell modules from PSGallery
      - Detects the current PowerShell version and offers to install the
        latest stable release via winget or direct MSI/PKG download
      - Detects the current .NET SDK version and offers to install the
        latest via winget or the official dotnet-install script
      - Works on Windows, macOS and Linux (best-effort)

.PARAMETER WorkspacePath
    Root of the PowerShell GUI workspace. Defaults to parent of script dir.

.PARAMETER OutputFolder
    Where to write the generated script. Defaults to
    <WorkspacePath>\~DOWNLOADS\INSTALLER-Scripts

.PARAMETER Open
    If set, opens the generated script in the default editor after creation.

.OUTPUTS
    [string] Full path to the generated installer script.
#>

[CmdletBinding()]
param(
    [string]$WorkspacePath,
    [string]$OutputFolder,
    [switch]$Open
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

# ── resolve paths ──────────────────────────────────────────────────
if ([string]::IsNullOrWhiteSpace($WorkspacePath)) {
    $WorkspacePath = Split-Path -Parent (Split-Path -Parent $MyInvocation.MyCommand.Path)
}
$reportPath = Join-Path $WorkspacePath '~REPORTS'

if ([string]::IsNullOrWhiteSpace($OutputFolder)) {
    $OutputFolder = Join-Path (Join-Path $WorkspacePath '~DOWNLOADS') 'INSTALLER-Scripts'
}
if (-not (Test-Path $OutputFolder)) {
    New-Item -ItemType Directory -Path $OutputFolder -Force | Out-Null
}

# ── locate latest module matrix JSON ───────────────────────────────
$matrixFiles = @(Get-ChildItem -Path $reportPath -Filter 'module-dependency-matrix-*.json' -File -ErrorAction SilentlyContinue |
    Where-Object { $_.Name -notlike '*-pointer*' } |
    Sort-Object Name -Descending)

$missingModules = @()
$mismatchModules = @()

if ($matrixFiles.Count -gt 0) {
    $matrixData = Get-Content $matrixFiles[0].FullName -Raw | ConvertFrom-Json
    foreach ($mod in $matrixData.modules) {
        $name = [string]$mod.module
        if ([string]::IsNullOrWhiteSpace($name)) { continue }
        $status = [string]$mod.status

        if ($status -like 'missing*') {
            $missingModules += [pscustomobject]@{
                Name            = $name
                RequiredVersion = if ($mod.requiredVersion) { [string]$mod.requiredVersion } else { $null }
                MinimumVersion  = if ($mod.minimumVersion)  { [string]$mod.minimumVersion }  else { $null }
            }
        } elseif ($status -like 'installed-*' -and $status -notlike '*workspace*') {
            $mismatchModules += [pscustomobject]@{
                Name            = $name
                RequiredVersion = if ($mod.requiredVersion) { [string]$mod.requiredVersion } else { $null }
                MinimumVersion  = if ($mod.minimumVersion)  { [string]$mod.minimumVersion }  else { $null }
                Installed       = if ($mod.installedVersion) { [string]$mod.installedVersion } else { 'unknown' }
                Status          = $status
            }
        }
    }
    Write-Host ("Module matrix loaded: {0} missing, {1} mismatched" -f $missingModules.Count, $mismatchModules.Count)
} else {
    Write-Warning "No module-dependency-matrix JSON found in $reportPath -- module section will be empty."
}

