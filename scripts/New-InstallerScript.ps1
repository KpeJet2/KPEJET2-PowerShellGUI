function Update-DotNetSdk {
    $dotnetCmd = Get-Command dotnet -ErrorAction SilentlyContinue
    if ($dotnetCmd) {
        $dotnetVer = & dotnet --version 2>&1
        Write-Host "  Current .NET SDK: $dotnetVer" -ForegroundColor Cyan
    } else {
        Write-Host "  .NET SDK not found on this machine." -ForegroundColor Red
    }

    $hasWinget = $null -ne (Get-Command winget -ErrorAction SilentlyContinue)

    if ($hasWinget) {
        Write-Host "  Checking winget for latest .NET SDK..." -ForegroundColor DarkGray
        try {
            if ($PSCmdlet.ShouldProcess('.NET SDK (winget)', 'Install/Upgrade')) {
                $result = winget upgrade --id Microsoft.DotNet.SDK.9 --source winget --accept-source-agreements --accept-package-agreements 2>&1 | Out-String
                if ($result -match 'No applicable update found') {
                    # Try install instead
                    winget install --id Microsoft.DotNet.SDK.9 --source winget --accept-source-agreements --accept-package-agreements 2>&1 | Out-String | Out-Null
                }
                Write-Host "  .NET SDK install/upgrade initiated via winget." -ForegroundColor Green
            } else {
                Write-Host "  [WhatIf] Would install/upgrade .NET SDK via winget" -ForegroundColor DarkYellow
            }
            return
        } catch {
            Write-Warning "  winget method failed: $($_.Exception.Message)"
        }
    }

    # Fallback: official dotnet-install script
    if ($PSVersionTable.Platform -eq 'Unix') {
        Write-Host "  Downloading dotnet-install.sh..." -ForegroundColor Yellow
        if ($PSCmdlet.ShouldProcess('dotnet-install.sh', 'Download and run')) {
            try {
                $installScript = Invoke-RestMethod -Uri 'https://dot.net/v1/dotnet-install.sh' -ErrorAction Stop
                $tempSh = [System.IO.Path]::GetTempFileName() + '.sh'
                Set-Content -Path $tempSh -Value $installScript -Encoding UTF8
                & bash $tempSh --channel LTS
                Write-Host "  .NET SDK installed." -ForegroundColor Green
            } catch {
                Write-Warning "  Install failed: $($_.Exception.Message)"
            }
        }
    } else {
        Write-Host "  Downloading dotnet-install.ps1..." -ForegroundColor Yellow
        if ($PSCmdlet.ShouldProcess('dotnet-install.ps1', 'Download and run')) {
            try {
                $installScript = Invoke-RestMethod -Uri 'https://dot.net/v1/dotnet-install.ps1' -ErrorAction Stop
                $tempPs1 = Join-Path $env:TEMP 'dotnet-install.ps1'
                Set-Content -Path $tempPs1 -Value $installScript -Encoding UTF8
                & $tempPs1 -Channel LTS
                Write-Host "  .NET SDK installed." -ForegroundColor Green
            } catch {
                Write-Warning "  Install failed: $($_.Exception.Message)"
            }
        }
    }
}

Update-DotNetSdk

