function Install-RequiredModule {
    param(
        [string]$Name,
        [string]$RequiredVersion,
        [string]$MinimumVersion
    )

    $existing = Get-Module -ListAvailable -Name $Name -ErrorAction SilentlyContinue |
        Sort-Object Version -Descending | Select-Object -First 1

    if ($existing) {
        $needsUpdate = $false
        if ($RequiredVersion) {
            try { if ([version]$existing.Version -ne [version]$RequiredVersion) { $needsUpdate = $true } } catch { $needsUpdate = $true }
        } elseif ($MinimumVersion) {
            try { if ([version]$existing.Version -lt [version]$MinimumVersion) { $needsUpdate = $true } } catch { $needsUpdate = $true }
        }
        if (-not $needsUpdate) {
            Write-Host "  OK : $Name ($($existing.Version))" -ForegroundColor Green
            return
        }
        Write-Host "  UPDATE: $Name installed=$($existing.Version)" -ForegroundColor Yellow -NoNewline
    } else {
        Write-Host "  INSTALL: $Name" -ForegroundColor Magenta -NoNewline
    }

    $installParams = @{ Name = $Name; Scope = 'CurrentUser'; Force = $true; ErrorAction = 'Stop' }
    if ($RequiredVersion) { $installParams['RequiredVersion'] = $RequiredVersion }
    elseif ($MinimumVersion) { $installParams['MinimumVersion'] = $MinimumVersion }

    if ($PSCmdlet.ShouldProcess($Name, 'Install-Module')) {
        try {
            Install-Module @installParams
            Write-Host " -> Done" -ForegroundColor Green
        } catch {
            Write-Host " -> FAILED: $($_.Exception.Message)" -ForegroundColor Red
        }
    } else {
        Write-Host " [WhatIf]" -ForegroundColor DarkYellow
    }
}

