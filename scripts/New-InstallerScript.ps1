function Update-PowerShellVersion {
    $current = $PSVersionTable.PSVersion
    Write-Host "  Current PowerShell: $current" -ForegroundColor Cyan

    # Try winget first (Windows)
    $hasWinget = $null -ne (Get-Command winget -ErrorAction SilentlyContinue)

    if ($hasWinget) {
        Write-Host "  Checking winget for latest PowerShell..." -ForegroundColor DarkGray
        try {
            $listOutput = winget list --id Microsoft.PowerShell --accept-source-agreements 2>&1 | Out-String
            $upgradeOutput = winget upgrade --id Microsoft.PowerShell --accept-source-agreements --accept-package-agreements 2>&1 | Out-String

            if ($upgradeOutput -match 'No applicable update found' -or $upgradeOutput -match 'No installed package found') {
                # Not installed via winget -- try install
                if ($PSCmdlet.ShouldProcess('PowerShell 7 (winget)', 'Install')) {
                    Write-Host "  Installing PowerShell via winget..." -ForegroundColor Yellow
                    winget install --id Microsoft.PowerShell --source winget --accept-source-agreements --accept-package-agreements
                    Write-Host "  PowerShell install initiated." -ForegroundColor Green
                } else {
                    Write-Host "  [WhatIf] Would install PowerShell via winget" -ForegroundColor DarkYellow
                }
            } elseif ($upgradeOutput -match 'Successfully installed' -or $upgradeOutput -match 'already installed') {
                Write-Host "  PowerShell is up to date via winget." -ForegroundColor Green
            } else {
                if ($PSCmdlet.ShouldProcess('PowerShell 7 (winget)', 'Upgrade')) {
                    Write-Host "  Upgrading PowerShell via winget..." -ForegroundColor Yellow
                    winget upgrade --id Microsoft.PowerShell --source winget --accept-source-agreements --accept-package-agreements
                    Write-Host "  Upgrade complete." -ForegroundColor Green
                } else {
                    Write-Host "  [WhatIf] Would upgrade PowerShell via winget" -ForegroundColor DarkYellow
                }
            }
            return
        } catch {
            Write-Warning "  winget method failed: $($_.Exception.Message)"
        }
    }

    # Fallback: direct install script from GitHub
    if ($PSVersionTable.Platform -eq 'Unix') {
        Write-Host "  Linux/macOS: use your package manager or https://aka.ms/install-powershell.sh" -ForegroundColor Yellow
        if ($PSCmdlet.ShouldProcess('PowerShell install script', 'Download and run')) {
            try {
                $installScript = Invoke-RestMethod -Uri 'https://aka.ms/install-powershell.sh' -ErrorAction Stop
                $tempSh = [System.IO.Path]::GetTempFileName() + '.sh'
                Set-Content -Path $tempSh -Value $installScript -Encoding UTF8
                Write-Host "  Install script saved to: $tempSh" -ForegroundColor Cyan
                Write-Host "  Run:  bash $tempSh -stable" -ForegroundColor Cyan
            } catch {
                Write-Warning "  Could not download install script: $($_.Exception.Message)"
            }
        }
    } else {
        Write-Host "  Windows (no winget): downloading MSI installer from GitHub..." -ForegroundColor Yellow
        if ($PSCmdlet.ShouldProcess('PowerShell MSI', 'Download')) {
            try {
                $releases = Invoke-RestMethod -Uri 'https://api.github.com/repos/PowerShell/PowerShell/releases/latest' -UseBasicParsing -ErrorAction Stop
                $msiAsset = $releases.assets | Where-Object { $_.name -match 'win-x64\.msi$' } | Select-Object -First 1
                if ($msiAsset) {
                    $msiPath = Join-Path $env:TEMP $msiAsset.name
                    Invoke-WebRequest -Uri $msiAsset.browser_download_url -OutFile $msiPath -UseBasicParsing -ErrorAction Stop
                    Write-Host "  Downloaded: $msiPath" -ForegroundColor Green
                    Write-Host "  Run the MSI to complete installation." -ForegroundColor Cyan
                    Start-Process $msiPath
                } else {
                    Write-Warning "  Could not find MSI asset in latest release."
                }
            } catch {
                Write-Warning "  Download failed: $($_.Exception.Message)"
            }
        }
    }
}

Update-PowerShellVersion

