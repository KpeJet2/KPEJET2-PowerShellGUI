        $minArg = if ($m.MinimumVersion)  { " -MinimumVersion '$($m.MinimumVersion)'" }   else { '' }
        [void]$sb.AppendLine("Install-RequiredModule -Name '$($m.Name)'$reqArg$minArg")
    }
    [void]$sb.AppendLine('')
    [void]$sb.AppendLine("# --- Mismatched modules ($($mismatchModules.Count)) ---")
